// Generates src/features/editor/lsp/completion-icons.css from @phosphor-icons/react dist.
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const defsDir = join(root, "node_modules", "@phosphor-icons", "react", "dist", "defs");
const outFile = join(root, "src", "features", "editor", "lsp", "completion-icons.css");

const ICONS = {
  class: "Cube",
  constant: "Pi",
  enum: "ListNumbers",
  function: "Function",
  interface: "Intersect",
  keyword: "Key",
  method: "BracketsRound",
  namespace: "Package",
  property: "Tag",
  text: "TextT",
  type: "TextAa",
  variable: "BracketsCurly",
};

const FILL = "#8b949e";

function extractRegularPaths(iconName) {
  const source = readFileSync(join(defsDir, `${iconName}.es.js`), "utf8");
  const start = source.indexOf('"regular"');
  const end = source.indexOf('"thin"');
  if (start === -1 || end === -1 || end <= start) {
    throw new Error(`Could not locate regular weight in ${iconName}`);
  }
  const section = source.slice(start, end);
  const paths = [...section.matchAll(/\{ d: "([^"]+)"/g)].map((match) => match[1]);
  if (paths.length === 0) {
    throw new Error(`No path data found for ${iconName}`);
  }
  return paths;
}

function toDataUri(paths) {
  const svg = `<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 256 256" fill="${FILL}">${paths
    .map((d) => `<path d="${d}"/>`)
    .join("")}</svg>`;
  return `url("data:image/svg+xml,${encodeURIComponent(svg)}")`;
}

const lines = [
  "/* Generated by scripts/generate-completion-icons.mjs — do not edit. */",
  ".cm-tooltip-autocomplete .cm-completionIcon {",
  "  width: 14px;",
  "  min-width: 14px;",
  "  margin-right: 4px;",
  "  background-size: contain;",
  "  background-repeat: no-repeat;",
  "  background-position: center;",
  "}",
  ".cm-tooltip-autocomplete .cm-completionIcon::after {",
  '  content: "";',
  "}",
];

for (const [type, iconName] of Object.entries(ICONS)) {
  lines.push(`.cm-tooltip-autocomplete .cm-completionIcon-${type} {`);
  lines.push(`  background-image: ${toDataUri(extractRegularPaths(iconName))};`);
  lines.push("}");
}

writeFileSync(outFile, lines.join("\n") + "\n");
console.log(`Wrote ${outFile}`);
